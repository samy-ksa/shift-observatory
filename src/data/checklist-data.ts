// Helper: filter items by country and sector
export function getFilteredChecklist(
  countryId: string,
  sectorId: string,
): ChecklistSection[] {
  return CHECKLIST_SECTIONS.map((section) => ({
    ...section,
    items: section.items.filter((item) => {
      const countryMatch = !item.countries || item.countries.includes(countryId);
      const sectorMatch = !item.sectors || item.sectors.includes(sectorId);
      return countryMatch && sectorMatch;
    }),
  })).filter((section) => section.items.length > 0);
}
