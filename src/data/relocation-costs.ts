export function getCostsByCategory(category: CostCategory): CostItem[] {
  return COST_DATABASE.filter((item) => item.category === category);
}

export function getCostForCity(
  itemId: string,
  cityId: CityId
): number | undefined {
  const item = COST_DATABASE.find((i) => i.id === itemId);
  return item?.prices[cityId];
}

export function getPriceTrends(): CostItem[] {
  return COST_DATABASE.filter(
    (item) => item.trend && item.trend !== "stable" && item.trendPct !== 0
  );
}

export function compareCities(
  cityA: CityId,
  cityB: CityId,
  category?: CostCategory
): {
  item: CostItem;
  priceA?: number;
  priceB?: number;
  diffPct: number;
}[] {
  const items = category ? getCostsByCategory(category) : COST_DATABASE;
  return items
    .filter(
      (item) =>
        item.prices[cityA] !== undefined && item.prices[cityB] !== undefined
    )
    .map((item) => {
      const priceA = item.prices[cityA]!;
      const priceB = item.prices[cityB]!;
      // Convert to SAR for comparison
      const cityARate = CITY_EXCHANGE_RATES[cityA];
      const cityBRate = CITY_EXCHANGE_RATES[cityB];
      const priceASAR = priceA * cityARate;
      const priceBSAR = priceB * cityBRate;
      const diffPct = ((priceBSAR - priceASAR) / priceASAR) * 100;
      return { item, priceA, priceB, diffPct: Math.round(diffPct) };
    });
}
